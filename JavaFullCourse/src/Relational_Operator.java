
public class Relational_Operator //It gives only True False
{

	public static void main(String[] args) 
	{
		int a=10,b=20;
		System.out.println(a==b); //10==20 (is not equal)
		System.out.println(a!=b);// 10!=20 (10 is not equal to 20)
		System.out.println(a>b);// 10>20 (10 is not greater than 20)
		System.out.println(a<b);// 10<20 (20 is greater than 10)
		System.out.println(a>=b);//10>=20(no)
		System.out.println(a<=b);//10<=20 (yes)
		

	}

}
