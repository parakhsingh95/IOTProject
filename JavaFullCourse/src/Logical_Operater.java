
public class Logical_Operater
{

	public static void main(String[] args) 
	{
		int a=10,b=20;
		System.out.println(a==b && a!=b);//First check only first condition
		
		System.out.println(a==b  || a!=b);//first check first condition if it is false then go next condition
		
		System.out.println(!(a>b));
		

	}

}
